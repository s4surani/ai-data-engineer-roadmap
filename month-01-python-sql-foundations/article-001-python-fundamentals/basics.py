# basics.py - Understanding Variables

# Variables are containers for storing data
# In Python, you don't need to declare types explicitly

# String (text data)
engineer_name = "Surani"
company = "AWS"
role = "AI Data Engineer"

print(f"My name is {engineer_name}")
print(f"I work at {company} as a {role}")

# Integer (whole numbers)
years_experience = 2
projects_completed = 15
team_size = 8

print(f"\nExperience: {years_experience} years")
print(f"Projects: {projects_completed}")

# Float (decimal numbers)
average_rating = 4.7
salary_lakhs = 18.5
project_success_rate = 0.93

print(f"\nAverage Rating: {average_rating}/5")
print(f"Success Rate: {project_success_rate * 100}%")

# Boolean (True/False)
is_certified = True
has_aws_experience = True
is_beginner = False

print(f"\nCertified: {is_certified}")
print(f"AWS Experience: {has_aws_experience}")

# None (represents absence of value)
current_project = None
print(f"\nCurrent Project: {current_project}")